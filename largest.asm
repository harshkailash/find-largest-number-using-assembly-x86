Find largest number
ASSUME CS:CODE, DS:DATA
DATA SEGMENT
ARRAY DW 00A5H, 0086H, 00B7H, 0032H, 0002H
COUNT EQU 05H
DATA ENDS

CODE SEGMENT
START:
MOV AX,DATA
MOV DS,AX
XOR AX,AX
MOV CX, COUNT-1
MOV SI, OFFSET ARRAY
MOV AL,[SI]

LOOP1: ADD SI,02H
CMP AL,[SI]
JNC LOOP2
MOV AL,[SI]

LOOP2: LOOP LOOP1

HLT
CODE ENDS
END START
